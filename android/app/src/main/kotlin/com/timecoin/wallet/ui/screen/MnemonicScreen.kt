                color: #666;
            }
            .word-grid .word {
                font-family: "Courier New", Courier, monospace;
                font-size: 12pt;
                width: 55mm;
            }
            .word-grid .gap { width: 10mm; }
            .verify-title {
                font-size: 12pt;
                font-weight: bold;
                margin: 6mm 0 3mm 0;
            }
            .verify-text {
                font-size: 9pt;
                line-height: 1.6;
            }
