                TIME Coin Wallet — https://time-coin.io<br>
                This document is confidential. Destroy securely when no longer needed.
            </div>
        </body>
        </html>
    """.trimIndent()

    val webView = android.webkit.WebView(context)
    webView.loadDataWithBaseURL(null, html, "text/html", "UTF-8", null)
    webView.createPrintDocumentAdapter("TIME-Recovery-Phrase").let { adapter ->
        printManager.print("TIME Recovery Phrase", adapter, null)
    }
}

@Composable
private fun MnemonicWordInput(
    number: Int,
    value: String,
    onValueChange: (String) -> Unit,
    isValid: Boolean?,
    focusRequester: FocusRequester,
    isLastField: Boolean,
    onNext: () -> Unit,
    modifier: Modifier = Modifier,
) {
    OutlinedTextField(
        value = value,
        onValueChange = onValueChange,
        modifier = modifier.focusRequester(focusRequester),
        singleLine = true,
        label = { Text("$number") },
        textStyle = MaterialTheme.typography.bodyLarge,
        keyboardOptions = KeyboardOptions(
            capitalization = KeyboardCapitalization.None,
            autoCorrect = false,
            keyboardType = KeyboardType.Text,
            imeAction = if (isLastField) ImeAction.Done else ImeAction.Next,
        ),
        keyboardActions = KeyboardActions(
            onNext = { onNext() },
        ),
        trailingIcon = {
            when {
                value.isBlank() -> {}
                isValid == true -> Icon(
                    Icons.Default.Check,
                    contentDescription = "Valid word",
                    tint = Color(0xFF4CAF50),
                    modifier = Modifier.size(20.dp),
                )
                isValid == false -> Icon(
                    Icons.Default.Close,
                    contentDescription = "Invalid word",
                    tint = MaterialTheme.colorScheme.error,
                    modifier = Modifier.size(20.dp),
                )
            }
        },
        isError = value.isNotBlank() && isValid == false,
    )
}
