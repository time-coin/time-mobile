                font-size: 10pt;
                color: #666;
                margin: 0 0 2mm 0;
            }
            hr { border: none; border-top: 0.5pt solid #c0c0c0; margin: 6mm 0; }
            .warning-box {
                margin: 4mm 0 6mm 0;
            }
            .warning-title {
                font-size: 12pt;
                font-weight: bold;
                margin-bottom: 3mm;
            }
            .warning-list {
                list-style: none;
                padding-left: 5mm;
                margin: 0;
            }
            .warning-list li {
                font-size: 9pt;
                line-height: 1.8;
            }
